"""Проблема размена монет: поиск количества способов 
внести сдачу на заданную сумму денег, используя заданный набор номиналов монет.


Дано:

Набор номиналов монет: coins = [c₁, c₂, ..., cₘ] (каждого вида бесконечно много)

Целевая сумма: S

Найти: Количество различных способов набрать сумму S монетами из набора.
"""


def coin_change_ways(coins, amount):
    """
    Находит количество способов размена суммы amount монетами coins
    Время: O(amount * len(coins)), Память: O(amount)
    """
    if amount < 0:
        return 0
    
    # dp[i] = количество способов набрать сумму i
    dp = [0] * (amount + 1)
    dp[0] = 1  # База динамики: 1 способ набрать нулевую сумму
    
    # Перебираем монеты
    for coin in coins:
        # Для каждой монеты обновляем dp
        for i in range(coin, amount + 1):
            dp[i] += dp[i - coin]
    
    return dp[amount]


# Примеры
print(coin_change_ways([1, 2, 5], 5))    # 4
print(coin_change_ways([2, 3, 5], 7))    # 2
print(coin_change_ways([1, 2, 3], 4))    # 4